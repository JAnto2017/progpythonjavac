# Comparaciones

print(f'a == b: {a == b}')  # Igualdad
print(f'a!= b: {a!= b}')  # Diferencia
print(f'a < b: {a < b}')  # Menor que
print(f'a <= b: {a <= b}')  # Menor o igual que
print(f'a > b: {a > b}')  # Mayor que



# Operadores Aritméticos

print('*** Operadores Aritméticos ***')

a,b = 2, 3
suma = a + b
resta = a - b
multiplicacion = a * b
division = a / b
modulo = a % b
potencia = a ** b

print(f'Valor de a: {a}')
print(f'Valor de b: {b}')
print(f'Resultado de la suma {a} + {b} = {suma}')
print(f'Resultado de la resta {a} - {b} = {resta}')
print(f'Resultado de la multiplicación {a} * {b} = {multiplicacion}')
print(f'Resultado de la division {a} - {b} = {division}')
print(f'Resultado del modulo {a} % {b} = {modulo}')
print(f'La potencia con base {a} y exponente {b} = {potencia}')


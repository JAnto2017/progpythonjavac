# Operador de Asignación Compuesto

a,b = 10, 15

print(f'Valor inicial de a: {a}')
print(f'Valor inicial de b: {b}')

# operador compuesto de suma

a += b
print(f'La suma de a:10 += b:{b} = {a}')

a -= b
print(f'La restado de a: -= b:{b} = {a}')

a *= b

print(f'La multiplicación de a: *= b:{b} = {a}')

a /= b

print(f'La división de a: /= b:{b} = {a}')